print('\nDemonstrate comparison operations')
print('-' * 50)

# Equality
eq = 3 == 3
print(f'{"Equality (==):":>18s} {eq} = 3 == 3')
# Not equal to
not_eq = 3 != 4
print(f'{"Not Equal to (!=):":>18s} {not_eq} = 3 != 4')
# Less than
less = 3 < 4
print(f'{"Less than (<):":>18s} {less} = 3 < 4')
# Greater than
greater = 5 > 1
print(f'{"Greater than (>):":>18s} {greater} = 5 > 1')
# Less than or equal to
less_eq = 9 <= 9
print(f'{"Less than (<=):":>18s} {less_eq} = 9 <= 9')
# Greater than or equal to
greater_eq = 18 >= 18
print(f'{"Greater than (>=):":>18s} {greater_eq} = 18 >= 18')