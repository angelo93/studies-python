print('Demonstrate arithmetic operations')
print('-' * 50)

# Addition
add = 3 + 3
print(f'{"Addition:":>15s} 3 + 3 = {add}')
# Subtraction
sub = 3 - 3
print(f'{"Subtraction:":>15s} 3 - 3 = {sub}')
# Multiplication
multi = 3 * 3
print(f'{"Multiplication:":>15s} 3 * 3 = {multi}')
# True Division
true_div = 10/ 3
print(f'{"True Division:":>15s} 4 / 3 = {true_div}')
# Floor Division
flr_div = 10 // 3
print(f'{"Floor Division:":>15s} 10 // 3 = {flr_div}')
# Exponentiation
exp = 4**4
print(f'{"Exponentiation:":>15s} 4**4 = {exp}')

